    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        
    String titulo = jTextFieldTitulo.getText();
    String duracionStr = jTextFieldDuracion.getText();
    String descripcion = jTextFieldDescripcion.getText();
    String idioma = (String) jComboBoxIdioma.getSelectedItem();
    String clasificacion = (String) jComboBoxClasificacion.getSelectedItem();

    if (titulo.isEmpty() || duracionStr.isEmpty() || descripcion.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos deben estar llenos.");
        return;
    }

    try (Connection conn = Conexion.getConexion()) {
        String sql = "INSERT INTO peliculas (titulo, idioma, duracion, clasificacion, descripcion) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement ps = conn.prepareStatement(sql);

        ps.setString(1, titulo);
        ps.setString(2, idioma);
        ps.setInt(3, Integer.parseInt(duracionStr));
        ps.setString(4, clasificacion);
        ps.setString(5, descripcion);

        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Película registrada correctamente.");

        cargarPeliculas();
        limpiarCampos();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al registrar película: " + e.getMessage());
    }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
         int fila = jTablePeliculas.getSelectedRow();
    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona una película para actualizar.");
        return;
    }

    int id = (int) modelo.getValueAt(fila, 0);
    String titulo = jTextFieldTitulo.getText();
    String duracionStr = jTextFieldDuracion.getText();
    String descripcion = jTextFieldDescripcion.getText();
    String idioma = (String) jComboBoxIdioma.getSelectedItem();
    String clasificacion = (String) jComboBoxClasificacion.getSelectedItem();

    try (Connection conn = Conexion.getConexion()) {
        String sql = "UPDATE peliculas SET titulo=?, idioma=?, duracion=?, clasificacion=?, descripcion=? WHERE id=?";
        PreparedStatement ps = conn.prepareStatement(sql);

        ps.setString(1, titulo);
        ps.setString(2, idioma);
        ps.setInt(3, Integer.parseInt(duracionStr));
        ps.setString(4, clasificacion);
        ps.setString(5, descripcion);
        ps.setInt(6, id);

        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Película actualizada correctamente.");

        cargarPeliculas();
        limpiarCampos();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al actualizar película: " + e.getMessage());
    }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
          int fila = jTablePeliculas.getSelectedRow();
    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Selecciona una película para eliminar.");
        return;
    }

    int id = (int) modelo.getValueAt(fila, 0);

    int confirm = JOptionPane.showConfirmDialog(this, "¿Estás seguro de eliminar la película?", "Confirmar", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        try (Connection conn = Conexion.getConexion()) {
            String sql = "DELETE FROM peliculas WHERE id=?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, id);

            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Película eliminada correctamente.");

            cargarPeliculas();
            limpiarCampos();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al eliminar película: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

