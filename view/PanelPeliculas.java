    private void cargarPeliculas() {
    modelo.setRowCount(0); // Limpiar la tabla antes de recargar

    try (Connection conn = Conexion.getConexion()) {
        String sql = "SELECT * FROM peliculas";
        PreparedStatement ps = conn.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        while (rs.next()) {
            Object[] fila = {
                rs.getInt("id"),
                rs.getString("titulo"),
                rs.getString("idioma"),
                rs.getInt("duracion"),
                rs.getString("clasificacion"),
                rs.getString("descripcion")
            };
            modelo.addRow(fila);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error al cargar películas: " + e.getMessage());
    }
}

    private void limpiarCampos() {
    jTextFieldTitulo.setText("");
    jTextFieldDuracion.setText("");
    jTextFieldDescripcion.setText("");
    jComboBoxIdioma.setSelectedIndex(0);
    jComboBoxClasificacion.setSelectedIndex(0);
    jTablePeliculas.clearSelection();
}
   

