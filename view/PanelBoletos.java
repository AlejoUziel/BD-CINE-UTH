    private void cargarAsientosDisponibles(int idFuncion) throws SQLException {
    jComboBoxAsiento.removeAllItems();
    BoletoDAO boletoDAO = new BoletoDAO(Conexion.getConexion());
    List<Integer> ocupados = boletoDAO.obtenerAsientosOcupados(idFuncion);
    
    for (int i = 1; i <= 50; i++) {
        if (!ocupados.contains(i)) {
            jComboBoxAsiento.addItem(String.valueOf(i));
        }
    }
}
    
    private void calcularPrecio() {
    double precioBase = 150.0;
    boolean esEstudiante = jComboBoxDescuentoEstudiante.getSelectedItem().equals("Sí");
    double precioFinal = esEstudiante ? precioBase * 0.4 : precioBase;
    jTextFieldPrecio.setText(String.valueOf(precioFinal));
}

private int obtenerIdFuncionSeleccionada() {
    String pelicula = jComboBoxPelicula.getSelectedItem().toString();
    String sala = jComboBoxSala.getSelectedItem().toString();
    String horario = jComboBoxHorario.getSelectedItem().toString();
    // Supón que tienes una clase FuncionDAO con método que obtiene el ID
    FuncionDAO dao = new FuncionDAO(Conexion.getConexion()) {};
    return dao.obtenerIdFuncion(pelicula, sala, horario);
}


