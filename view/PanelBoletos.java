    private void jButtonConfirmarCompraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarCompraActionPerformed
       try {
        String cliente = txtFieldCliente.getText();
        String metodoPago = jComboBoxMetodoPago.getSelectedItem().toString();
        boolean esEstudiante = jComboBoxDescuentoEstudiante.getSelectedItem().toString().equalsIgnoreCase("Sí");
        double precioBase = Double.parseDouble(jTextFieldPrecio.getText());

        if (cliente.isEmpty()) {
            jLabelResultado.setText("Debe ingresar el nombre o ID del cliente.");
            return;
        }

        int idFuncion = obtenerIdFuncionSeleccionada();  // implementa según tu lógica
        int idAsiento = obtenerIdAsientoSeleccionado();  // implementa según tu lógica

        double precioFinal = esEstudiante ? precioBase * 0.4 : precioBase;
        String fecha = java.time.LocalDate.now().toString();

        Boleto boleto = new Boleto(idFuncion, idAsiento, cliente, precioFinal, metodoPago, esEstudiante, fecha);
        BoletosDAO dao = new BoletosDAO(ConexionBD.getConnection());

        boolean insertado = dao.insertarBoleto(boleto);
        boolean asientoActualizado = dao.marcarAsientoComoOcupado(idAsiento);

        if (insertado && asientoActualizado) {
            jLabelResultado.setText("Boleto vendido con éxito. Precio: L " + precioFinal);
        } else {
            jLabelResultado.setText("Error al vender el boleto.");
        }
    } catch (Exception ex) {
        ex.printStackTrace();
        jLabelResultado.setText("Error inesperado: " + ex.getMessage());
    }
    }//GEN-LAST:event_jButtonConfirmarCompraActionPerformed

